def display_deadline(record: dict[str, Any]) -> str:
    return (
        clean(
            nested(
                record,
                "dates",
                "application_deadline",
                "display",
            )
        )
        or clean(
            nested(
                record,
                "dates",
                "application_deadline",
                "normalized",
            )
        )
        or clean(
            nested(
                record,
                "dates",
                "application_deadline",
                "raw",
            )
        )
        or "Not confirmed"
    )


def display_status(
    record: dict[str, Any],
    *,
    today: date,
) -> str:
    if is_rolling(record):
        return "ð **ROLLING**"

    deadline = normalized_deadline(record)
    if deadline is None:
        return "â ï¸ **VERIFY**"

    days_remaining = (deadline - today).days
    if days_remaining <= CLOSING_SOON_DAYS:
        return "ð¥ **CLOSING SOON**"

    return "â **OPEN**"


def first_date_text(
    value: Any,
) -> str | None:
    if not isinstance(value, dict):
        return None

    return (
        clean(value.get("display"))
        or clean(value.get("normalized"))
        or clean(value.get("raw"))
    )


def display_program_dates(record: dict[str, Any]) -> str:
    start = first_date_text(
        nested(record, "dates", "start_date")
    )
    end = first_date_text(
        nested(record, "dates", "end_date")
    )

    if start and end and start != end:
        return f"{start} â {end}"
    if start:
        return start
    if end:
        return end
    return "Dates not confirmed"


def display_location(record: dict[str, Any]) -> str:
    display = clean(nested(record, "location", "display"))
    if display:
        return display

    city = clean(nested(record, "location", "host_city"))
    country = clean(
        nested(record, "location", "host_country")
    )
    combined = ", ".join(
        value for value in (city, country) if value
    )
    if combined:
        return combined

    if clean(record.get("format")) == "online":
        return "Online"

    return "Location not confirmed"


def display_when_where(record: dict[str, Any]) -> str:
    return (
        f"{markdown_text(display_program_dates(record))}"
        "<br>"
        f"{markdown_text(display_location(record))}"
    )


def display_focus(record: dict[str, Any]) -> str:
    fields = unique_strings(
        nested(record, "filters", "academic_fields")
    )
    subjects = unique_strings(
        nested(record, "filters", "subjects")
    )
    topics = unique_strings(
        nested(record, "filters", "topics")
    )

    selected = fields[:2] or subjects[:2] or topics[:2]

    if not selected:
        return "Open / multidisciplinary"

    return ", ".join(
        markdown_text(humanize(item))
        for item in selected[:MAX_CELL_ITEMS]
    )


def display_funding(record: dict[str, Any]) -> str:
    features = unique_strings(
        nested(record, "filters", "funding_features")
    )

    if not features:
        return "Not confirmed"

    return ", ".join(
        markdown_text(humanize(item))
        for item in features[:MAX_CELL_ITEMS]
    )


def display_eligibility(record: dict[str, Any]) -> str:
    levels = unique_strings(
        nested(record, "filters", "academic_levels")
    )
    regions = unique_strings(
        nested(record, "filters", "eligible_regions")
    )
    audiences = unique_strings(
        nested(record, "filters", "audience_groups")
    )

    selected = (
        levels[:2]
        + regions[:1]
        + audiences[:1]
    )

    if not selected:
        return "See opportunity page"

    return ", ".join(
        markdown_text(humanize(item))
        for item in selected[:MAX_CELL_ITEMS]
    )


def safe_external_url(value: Any) -> str | None:
    text = clean(value)
    if text is None:
        return None

    parsed = urlparse(text)
    if parsed.scheme not in {"http", "https"}:
        return None
    if not parsed.hostname:
        return None

    return text


def application_link(record: dict[str, Any]) -> str:
    application_url = safe_external_url(
        nested(
            record,
            "application",
            "application_page",
        )
    )
    official_url = safe_external_url(
        nested(
            record,
            "application",
            "official_page",
        )
    )

    if application_url:
        return markdown_link("Apply", application_url)
    if official_url:
        return markdown_link(
            "Official page",
            official_url,
        )

    return "Not confirmed"


def opportunity_link(
    record: dict[str, Any],
    *,
    page_path: Path,
) -> str:
    title = (
        clean(record.get("title"))
        or "Untitled Opportunity"
    )
    path_value = clean(record.get("path"))
    if path_value is None:
        fail(f"Indexed opportunity '{title}' has no path.")

    target = ROOT / path_value
    relative = relative_link(page_path, target)
    return markdown_link(title, relative)


def build_table(
    records: list[dict[str, Any]],
    *,
    page_path: Path,
    category_title: str,
    today: date,
) -> list[str]:
    lines = [
        (
            "| Status | Category | Opportunity | Focus | "
            "When & Where | Format | Funding | Eligibility | "
            "Apply | Deadline |"
        ),
        (
            "|---|---|---|---|---|---|---|---|---|---|"
        ),
    ]

    for record in records:
        opportunity_format = (
            clean(record.get("format"))
            or "not-confirmed"
        )

        cells = [
            display_status(record, today=today),
            markdown_text(category_title),
            opportunity_link(
                record,
                page_path=page_path,
            ),
            display_focus(record),
            display_when_where(record),
            markdown_text(humanize(opportunity_format)),
            display_funding(record),
            display_eligibility(record),
            application_link(record),
            markdown_text(display_deadline(record)),
        ]

        lines.append("| " + " | ".join(cells) + " |")

    return lines


