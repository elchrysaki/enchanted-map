def load_index() -> list[dict[str, Any]]:
    if not INDEX_PATH.is_file():
        fail(
            f"Missing central opportunity index: "
            f"{INDEX_PATH.relative_to(ROOT)}. "
            "Run scripts/build_indexes.py first."
        )

    try:
        payload = json.loads(
            INDEX_PATH.read_text(encoding="utf-8")
        )
    except (OSError, json.JSONDecodeError) as exc:
        fail(f"Could not read the central opportunity index: {exc}")

    index = require_mapping(
        payload,
        context="Opportunity index",
    )

    if index.get("schema_version") != EXPECTED_INDEX_SCHEMA_VERSION:
        fail(
            "data/opportunities.json must use "
            f"schema_version {EXPECTED_INDEX_SCHEMA_VERSION}."
        )

    if index.get("record_type") != EXPECTED_INDEX_RECORD_TYPE:
        fail(
            "data/opportunities.json must have "
            "record_type 'opportunity-index'."
        )

    opportunities = index.get("opportunities")
    if not isinstance(opportunities, list):
        fail("data/opportunities.json opportunities must be a list.")

    records: list[dict[str, Any]] = []

    for index_number, item in enumerate(opportunities):
        if not isinstance(item, dict):
            fail(
                "data/opportunities.json opportunity "
                f"#{index_number + 1} must be an object."
            )

        if clean(item.get("status")) != "published":
            warn(
                "Ignoring an indexed record whose status is not "
                f"published: {item.get('title', 'Untitled')}"
            )
            continue

        records.append(item)

    return records


