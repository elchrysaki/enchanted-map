def is_rolling(record: dict[str, Any]) -> bool:
    candidates = (
        nested(
            record,
            "dates",
            "application_deadline",
            "raw",
        ),
        nested(
            record,
            "dates",
            "application_deadline",
            "display",
        ),
    )

    return any(
        "rolling" in text.casefold()
        for value in candidates
        if (text := clean(value)) is not None
    )


def normalized_deadline(
    record: dict[str, Any],
) -> date | None:
    return parse_iso_date(
        nested(
            record,
            "dates",
            "application_deadline",
            "normalized",
        )
    )


def is_active(
    record: dict[str, Any],
    *,
    today: date,
) -> bool:
    if is_rolling(record):
        return True

    deadline = normalized_deadline(record)
    if deadline is None:
        return True

    return deadline >= today


def deadline_sort_key(
    record: dict[str, Any],
) -> tuple[int, date, str]:
    deadline = normalized_deadline(record)
    title = (
        clean(record.get("title"))
        or "Untitled Opportunity"
    ).casefold()

    if deadline is not None:
        return (0, deadline, title)

    if is_rolling(record):
        return (1, date.max, title)

    return (2, date.max, title)


def group_active_records(
    records: list[dict[str, Any]],
    *,
    navigation: list[str],
    main_categories: dict[str, dict[str, Any]],
    today: date,
) -> dict[str, dict[str, list[dict[str, Any]]]]:
    grouped: dict[
        str,
        dict[str, list[dict[str, Any]]],
    ] = {
        main_key: {
            category_key: []
            for category_key in main_categories[
                main_key
            ]["categories"]
        }
        for main_key in navigation
    }

    ignored_counts: dict[str, int] = {}

    for record in records:
        main_key = clean(record.get("main_category"))
        category_key = clean(record.get("category"))

        if main_key not in grouped:
            ignored_key = main_key or "missing-main-category"
            ignored_counts[ignored_key] = (
                ignored_counts.get(ignored_key, 0) + 1
            )
            continue

        if category_key not in grouped[main_key]:
            fail(
                f"Published record "
                f"'{record.get('title', 'Untitled')}' uses "
                f"category '{category_key}' under '{main_key}', "
                "but config/categories.yml does not define that pair."
            )

        if not is_active(record, today=today):
            continue

        grouped[main_key][category_key].append(record)

    for main_key in navigation:
        for category_key in grouped[main_key]:
            grouped[main_key][category_key].sort(
                key=deadline_sort_key
            )

    for ignored_key, count in sorted(ignored_counts.items()):
        warn(
            f"Ignoring {count} published record(s) from "
            f"non-browsed main category '{ignored_key}'."
        )

    return grouped


