def fail(message: str) -> None:
    print(f"::error::{message}")
    raise SystemExit(1)


def warn(message: str) -> None:
    print(f"::warning::{message}")


def clean(value: Any) -> str | None:
    if value is None:
        return None
    if isinstance(value, (date, datetime)):
        return value.isoformat()
    if not isinstance(value, str):
        return None
    result = " ".join(value.split())
    return result or None


def nested(record: dict[str, Any], *keys: str) -> Any:
    value: Any = record
    for key in keys:
        if not isinstance(value, dict):
            return None
        value = value.get(key)
    return value


def require_mapping(
    value: Any,
    *,
    context: str,
) -> dict[str, Any]:
    if not isinstance(value, dict):
        fail(f"{context} must be a YAML/JSON object.")
    return value


def require_string(
    value: Any,
    *,
    context: str,
) -> str:
    result = clean(value)
    if result is None:
        fail(f"{context} must contain text.")
    return result


def require_string_list(
    value: Any,
    *,
    context: str,
) -> list[str]:
    if not isinstance(value, list):
        fail(f"{context} must be a list.")
    result: list[str] = []
    for index, item in enumerate(value):
        result.append(
            require_string(
                item,
                context=f"{context}[{index}]",
            )
        )
    return result


def humanize(value: str) -> str:
    return (
        value.replace("-", " ")
        .replace("_", " ")
        .title()
    )


def unique_strings(value: Any) -> list[str]:
    if not isinstance(value, list):
        return []

    result: list[str] = []
    seen: set[str] = set()

    for item in value:
        text = clean(item)
        if text is None:
            continue
        key = text.casefold()
        if key in seen:
            continue
        seen.add(key)
        result.append(text)

    return result


def parse_iso_date(value: Any) -> date | None:
    text = clean(value)
    if text is None:
        return None

    try:
        parsed = datetime.strptime(text, "%Y-%m-%d").date()
    except ValueError:
        return None

    if parsed.isoformat() != text:
        return None

    return parsed


def markdown_text(
    value: Any,
    fallback: str = "Not confirmed",
) -> str:
    text = clean(value) or fallback
    return (
        text.replace("\\", "\\\\")
        .replace("|", "\\|")
        .replace("[", "\\[")
        .replace("]", "\\]")
        .replace("\n", " ")
    )


def markdown_link(label: str, target: str) -> str:
    safe_label = markdown_text(label, fallback="Link")
    return f"[{safe_label}](<{target}>)"


def relative_link(
    from_page: Path,
    target: Path,
    *,
    directory: bool = False,
) -> str:
    relative = os.path.relpath(
        target,
        start=from_page.parent,
    )
    value = Path(relative).as_posix()
    if directory and not value.endswith("/"):
        value += "/"
    return value


def atomic_write(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    temporary = path.with_suffix(path.suffix + ".tmp")
    temporary.write_text(content, encoding="utf-8")
    temporary.replace(path)


