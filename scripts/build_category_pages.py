def load_config() -> tuple[
    list[str],
    dict[str, dict[str, Any]],
]:
    if not CONFIG_PATH.is_file():
        fail(
            f"Missing category configuration: "
            f"{CONFIG_PATH.relative_to(ROOT)}"
        )

    try:
        payload = yaml.safe_load(
            CONFIG_PATH.read_text(encoding="utf-8")
        )
    except (OSError, yaml.YAMLError) as exc:
        fail(f"Could not read category configuration: {exc}")

    config = require_mapping(
        payload,
        context="Category configuration",
    )

    if config.get("schema_version") != EXPECTED_CONFIG_SCHEMA_VERSION:
        fail(
            "config/categories.yml must use "
            f"schema_version {EXPECTED_CONFIG_SCHEMA_VERSION}."
        )

    navigation = require_string_list(
        config.get("navigation"),
        context="config.navigation",
    )

    if len(set(navigation)) != len(navigation):
        fail("config.navigation contains duplicate categories.")

    raw_main_categories = require_mapping(
        config.get("main_categories"),
        context="config.main_categories",
    )

    main_categories: dict[str, dict[str, Any]] = {}

    for main_key in navigation:
        raw_main = require_mapping(
            raw_main_categories.get(main_key),
            context=f"config.main_categories.{main_key}",
        )

        title = require_string(
            raw_main.get("title"),
            context=f"{main_key}.title",
        )
        emoji = require_string(
            raw_main.get("emoji"),
            context=f"{main_key}.emoji",
        )
        description = require_string(
            raw_main.get("description"),
            context=f"{main_key}.description",
        )
        raw_categories = require_mapping(
            raw_main.get("categories"),
            context=f"{main_key}.categories",
        )

        categories: dict[str, dict[str, str]] = {}
        used_routes: set[str] = set()

        for category_key, raw_category_value in raw_categories.items():
            category_key = require_string(
                category_key,
                context=f"{main_key} category key",
            )
            raw_category = require_mapping(
                raw_category_value,
                context=(
                    f"config.main_categories.{main_key}."
                    f"categories.{category_key}"
                ),
            )

            category_title = require_string(
                raw_category.get("title"),
                context=f"{main_key}.{category_key}.title",
            )
            category_emoji = require_string(
                raw_category.get("emoji"),
                context=f"{main_key}.{category_key}.emoji",
            )
            route = require_string(
                raw_category.get("route"),
                context=f"{main_key}.{category_key}.route",
            )
            category_description = require_string(
                raw_category.get("description"),
                context=f"{main_key}.{category_key}.description",
            )

            if "/" in route or route in {".", ".."}:
                fail(
                    f"{main_key}.{category_key}.route must be "
                    "one safe folder name."
                )

            if route in used_routes:
                fail(
                    f"{main_key} contains duplicate route "
                    f"'{route}'."
                )
            used_routes.add(route)

            categories[category_key] = {
                "title": category_title,
                "emoji": category_emoji,
                "route": route,
                "description": category_description,
            }

        main_categories[main_key] = {
            "title": title,
            "emoji": emoji,
            "description": description,
            "categories": categories,
        }

    return navigation, main_categories


