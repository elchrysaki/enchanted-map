def build_navigation(
    *,
    page_path: Path,
    navigation: list[str],
    main_categories: dict[str, dict[str, Any]],
) -> list[str]:
    home_link = relative_link(
        page_path,
        ROOT / "README.md",
    )
    contributing_link = relative_link(
        page_path,
        ROOT / "CONTRIBUTING.md",
    )

    links = [
        markdown_link("ð  Home", home_link),
        markdown_link("ð¤ Contributing", contributing_link),
    ]

    for main_key in navigation:
        main = main_categories[main_key]
        target = OPPORTUNITIES_DIR / main_key
        route = relative_link(
            page_path,
            target,
            directory=True,
        )
        links.append(
            markdown_link(
                f"{main['emoji']} {main['title']}",
                route,
            )
        )

    midpoint = (len(links) + 1) // 2

    return [
        '<div align="center">',
        "",
        " Â· ".join(links[:midpoint]),
        "",
        " Â· ".join(links[midpoint:]),
        "",
        "</div>",
    ]


def empty_state(category_title: str) -> list[str]:
    submission_url = (
        "https://github.com/elchrysaki/offmap-hub/"
        "issues/new?template=submit-opportunity.yml"
    )

    return [
        (
            f"> No active {category_title.lower()} "
            "have been published yet."
        ),
        ">",
        (
            f"> {markdown_link('Add one to the map â', submission_url)}"
        ),
    ]


def build_main_category_page(
    *,
    main_key: str,
    main: dict[str, Any],
    category_records: dict[
        str,
        list[dict[str, Any]],
    ],
    navigation: list[str],
    main_categories: dict[str, dict[str, Any]],
    today: date,
) -> tuple[Path, str]:
    page_path = (
        OPPORTUNITIES_DIR
        / main_key
        / "README.md"
    )

    total = sum(
        len(records)
        for records in category_records.values()
    )

    lines: list[str] = [
        GENERATED_MARKER,
        "",
        *build_navigation(
            page_path=page_path,
            navigation=navigation,
            main_categories=main_categories,
        ),
        "",
        "---",
        "",
        '<a id="top"></a>',
        "",
        f"# {main['emoji']} {main['title']}",
        "",
        main["description"],
        "",
        (
            f"> **{total} active "
            f"{main['title'].lower()} currently on the map.**"
        ),
        "",
    ]

    for category_key, category in main[
        "categories"
    ].items():
        records = category_records[category_key]
        preview = records[:PREVIEW_LIMIT]

        lines.extend(
            [
                "---",
                "",
                (
                    f"## {category['emoji']} "
                    f"{category['title']}"
                ),
                "",
                category["description"],
                "",
            ]
        )

        if preview:
            lines.extend(
                build_table(
                    preview,
                    page_path=page_path,
                    category_title=category["title"],
                    today=today,
                )
            )
        else:
            lines.extend(
                empty_state(category["title"])
            )

        lines.extend(
            [
                "",
                (
                    "**"
                    + markdown_link(
                        "View all active "
                        + category["title"].lower()
                        + " â",
                        category["route"] + "/",
                    )
                    + "**"
                ),
                "",
                (
                    f"<sub>Showing {len(preview)} of "
                    f"{len(records)} active listings.</sub>"
                    if records
                    else ""
                ),
                "",
                (
                    '<p align="right">'
                    '<a href="#top">â Back to the top</a>'
                    '</p>'
                ),
                "",
            ]
        )

    lines.extend(
        [
            "---",
            "",
            (
                "_Generated from `data/opportunities.json` by "
                "`scripts/build_category_pages.py`. "
                "Always verify details on the official page._"
            ),
            "",
        ]
    )

    return page_path, "\n".join(
        line for line in lines
        if line is not None
    )


